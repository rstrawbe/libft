int		ft_atoi(char *str)
{
	int res;
	int sign;

	res = 0;
	sign = 1;
	while (*str && (*str == 32 || (*str > 8 && *str < 14)))
		str++;
	if (*str == 45)
		sign = -1;
	if (*str == 45 || *str == 43)
		str++;
	while (*str != 0 && *str > 47 && *str < 58)
	{
		res = (res * 10) + (*str - 48);
		str++;
	}
	return (res * sign);
}
