#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;
	char	*p;

	p = (char *)s;
	if (!p)
	    return (0);
	i = 0;
	while (p && *(p++) != '\0')
		i++;
	return (i);
}
