char	*ft_strrchr(const char *s, int c)
{
	char	*i;
	char	*res;

	i = (char *)s;
	res = (0);
	while (*i)
	{
		if (*(i) == (char)c)
			res = i;
		i++;
	}
	if (c == 0)
		res = i;
	return (res);
}
